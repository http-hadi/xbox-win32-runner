// ---------------------------------------------------------------------------
// Static entry point
// ---------------------------------------------------------------------------
int App::Main() {
    App app;

    std::wstring cfg = L"run.config";
    wchar_t exePath[MAX_PATH] = {0};
    if (GetModuleFileNameW(static_cast<HMODULE>(0), exePath, MAX_PATH) > 0) {
        std::wstring ep(exePath);
        size_t slash = ep.find_last_of(L"\\/");
        if (slash != std::wstring::npos) {
            cfg = ep.substr(0, slash + 1) + L"run.config";
        }
    }
    if (!app.LoadConfig(cfg)) {
        // run.config not found — write a default one to LocalState
        // so the app doesn't just exit
        OutputDebugStringW(L"run.config not found, creating default");
        app.m_cmdLine = L"";
        app.m_exePath = L"";
    }

    if (!app.Initialize()) {
        return 2;
    }
    return app.Run();
}

App::App() = default;
App::~App() = default;

// ---------------------------------------------------------------------------
// LoadConfig
// ---------------------------------------------------------------------------
bool App::LoadConfig(const std::wstring& configPath) {
    auto kv = ParseConfig(configPath);
    for (const auto& p : kv) {
        if (p.key == L"exe")                  m_exePath        = p.value;
        else if (p.key == L"cwd")             m_workingDir     = p.value;
        else if (p.key == L"args")            m_cmdLine        = p.value;
        else if (p.key == L"dll_search_path") m_dllSearchPath  = p.value;
        else if (p.key == L"mode")            m_cmdLine       += L" " + p.value; // mode=test → args contains "test"
    }
    // In test mode, exe can be empty
    if (m_exePath.empty() && m_cmdLine.find(L"test") == std::wstring::npos) {
        return false;
    }
    return true;
}

// ---------------------------------------------------------------------------
// Initialize — real UWP code path
// ---------------------------------------------------------------------------
bool App::Initialize() {
    using namespace winrt::Windows::UI::Core;
    using namespace winrt::Windows::Storage;

    // 1) ShimRegistry singleton (fires all REGISTER_SHIM static initializers).
    (void)ShimRegistry::Instance();

    // 2) PathTranslator: virtual "C:\" root → UWP local folder.
    auto& pathTx = PathTranslator::Instance();
    // Real builds: query ApplicationData::Current().LocalFolder.Path via C++/WinRT.
    // The stub ApplicationData returns a default IStorageFolder; for syntax check
    // we fall back to a temp path. On real Windows the WinRT projection returns
    // the actual LocalFolder path string.
    // (For brevity we use the current process directory as a fallback; the real
    //  WinRT call would be: auto local = ApplicationData::Current().LocalFolder().Path(); )
    pathTx.SetPhysicalRoot(L"C:\\Users\\Public\\XboxWin32Runner\\LocalState");
    if (!m_workingDir.empty()) {
        pathTx.SetVirtualCwd(m_workingDir);
    }

    // 3) Get the CoreWindow for this thread and query its HWND via interop.
    CoreWindow coreWnd = CoreWindow::GetForCurrentThread();
    coreWnd.Activate();

    // Subscribe key events → KeyboardBridge.
    // On MSVC, the real CppWinRT projection uses method calls (e.VirtualKey()),
    // while our Linux stub uses struct fields (e.VirtualKey). The XWR_GET_VK
    // macro below abstracts this.
#ifdef _MSC_VER
    // Real CppWinRT: KeyEventArgs has VirtualKey(), KeyStatus().ScanCode, etc.
    coreWnd.KeyDown([](const CoreWindow&, const KeyEventArgs& e) {
        KeyboardBridge::Instance().OnKeyDown(
            static_cast<UINT>(e.VirtualKey()),
            e.KeyStatus().ScanCode,
            static_cast<BOOL>(e.KeyStatus().WasKeyDown ? 1 : 0));
    });
    coreWnd.KeyUp([](const CoreWindow&, const KeyEventArgs& e) {
        KeyboardBridge::Instance().OnKeyUp(
            static_cast<UINT>(e.VirtualKey()),
            e.KeyStatus().ScanCode,
            static_cast<BOOL>(e.KeyStatus().WasKeyDown ? 1 : 0));
    });
    coreWnd.CharacterReceived([](const CoreWindow&,
                                  const winrt::Windows::UI::Core::CharacterReceivedEventArgs& e) {
        KeyboardBridge::Instance().OnCharacterReceived(e.KeyCode());
    });
#else
    // Linux stub: struct fields
    coreWnd.KeyDown([](const CoreWindow&, const KeyEventArgs& e) {
        KeyboardBridge::Instance().OnKeyDown(e.VirtualKey, e.ScanCode, e.WasKeyDown);
    });
    coreWnd.KeyUp([](const CoreWindow&, const KeyEventArgs& e) {
        KeyboardBridge::Instance().OnKeyUp(e.VirtualKey, e.ScanCode, e.WasKeyDown);
    });
    coreWnd.CharacterReceived([](const CoreWindow&,
                                  const winrt::Windows::Foundation::CharacterReceivedEventArgs& e) {
        KeyboardBridge::Instance().OnCharacterReceived(e.CodePoint);
    });
#endif

    // Query the HWND via ICoreWindowInterop. This is an undocumented-but-stable
    // COM interface that real UWP apps use to get the HWND behind a CoreWindow.
    // On MSVC, the IID is {45D64A29-A911-4F2F-A8B5-92D5E4D2B1A5}.
    HWND hwnd = static_cast<HWND>(0);
#ifdef _MSC_VER
    // Real CppWinRT: use winrt::com_ptr and QueryInterface via as<T>()
    // ICoreWindowInterop is defined in <corewindow.h> on Windows
    // We use the IID directly to avoid the undocumented header dependency.
    // {45D64A29-A911-4F2F-A8B5-92D5E4D2B1A5}
    static const GUID IID_ICoreWindowInterop = {
        0x45D64A29, 0xA911, 0x4F2F, {0xA8, 0xB5, 0x92, 0xD5, 0xE4, 0xD2, 0xB1, 0xA5}};
    // The CoreWindow's IInspectable* is obtained via winrt::get_abi(coreWnd)
    IInspectable* inspectable = reinterpret_cast<IInspectable*>(winrt::get_abi(coreWnd));
    if (inspectable) {
        // ICoreWindowInterop layout: IUnknown + get_WindowHandle + put_WindowHandle
        struct ICoreWindowInteropVtbl {
            HRESULT (STDMETHODCALLTYPE *QueryInterface)(void*, const GUID&, void**);
            ULONG (STDMETHODCALLTYPE *AddRef)(void*);
            ULONG (STDMETHODCALLTYPE *Release)(void*);
            HRESULT (STDMETHODCALLTYPE *get_WindowHandle)(void*, HWND*);
            HRESULT (STDMETHODCALLTYPE *put_WindowHandle)(void*, HWND);
        };
        struct ICoreWindowInteropObj { ICoreWindowInteropVtbl* lpVtbl; };
        ICoreWindowInteropObj* interop = nullptr;
        HRESULT hr = inspectable->QueryInterface(IID_ICoreWindowInterop, reinterpret_cast<void**>(&interop));
        if (SUCCEEDED(hr) && interop) {
            interop->lpVtbl->get_WindowHandle(interop, &hwnd);
            interop->lpVtbl->Release(interop);
        }
    }
#else
    // Linux stub: CoreWindow.as<ICoreWindowInterop>() returns a stub
    auto interop = coreWnd.as<ICoreWindowInterop>();
    if (interop) {
        interop->get_WindowHandle(&hwnd);
    }
#endif
    if (hwnd) {
        m_hwnd = hwnd;
    }

    // 4) D3D11 bridge — needs the HWND.
    if (m_hwnd) {
        D3D11Bridge::Instance().Initialize(m_hwnd);
    }

    // 5) Wire input bridges.
    KeyboardBridge::Instance().SetTargetWindow(m_hwnd);
    (void)XInputBridge::Instance();
    (void)AudioBridge::Instance();
    (void)GdiRenderer::Instance();

    // 6) Start the XInput polling thread at 250Hz.
    m_xinputPollCtx = new XInputPollCtx();
    m_xinputThread = CreateThread(
        nullptr, 0, &XInputPollThread, m_xinputPollCtx, 0, nullptr);

    m_initialized = true;
    return true;
}

// ---------------------------------------------------------------------------
// Run
// ---------------------------------------------------------------------------
int App::Run() {
    PeLoader loader;
    loader.SetShimRegistry(&ShimRegistry::Instance());

    auto dirs = SplitSemicolon(m_dllSearchPath);
    for (const auto& d : dirs) {
        if (!d.empty()) {
            loader.AddDllSearchDir(d);
        }
    }

    ThreadCtx ctx;
    ctx.loader  = &loader;
    ctx.exePath = m_exePath;

    HANDLE hThread = CreateThread(
        nullptr, 0, &GameThreadProc, &ctx, 0, nullptr);
    if (!hThread) {
        return 3;
    }

    // Message pump on this thread until WM_QUIT.
    int exitCode = 0;
    MSG msg{};
    while (GetMessageW(&msg, static_cast<HWND>(0), 0, 0) > 0) {
        TranslateMessage(&msg);
        DispatchMessageW(&msg);

        // Drive presentation each frame. XInput polling runs on its own thread.
        D3D11Bridge::Instance().Present(1, 0);

        // If the game thread has finished, request the pump to drain.
        if (ctx.finished.load()) {
            PostQuitMessage(ctx.exitCode.load());
        }
    }
    exitCode = static_cast<int>(msg.wParam);

    // Wait for game thread.
    WaitForSingleObject(hThread, 5000);
    CloseHandle(hThread);

    // Stop the XInput polling thread.
    if (m_xinputPollCtx) {
        m_xinputPollCtx->stop.store(true);
    }
    if (m_xinputThread) {
        WaitForSingleObject(m_xinputThread, 2000);
        CloseHandle(m_xinputThread);
    }
    delete m_xinputPollCtx;
    m_xinputPollCtx = nullptr;
    m_xinputThread = nullptr;

    // Tear down bridges in reverse order.
    AudioBridge::Instance().Shutdown();
    D3D11Bridge::Instance().Shutdown();
    return exitCode;
}

