// uwp-shell/App.cpp
// Implementation of the XboxWin32Runner UWP shell application class.
//
// This file contains the REAL UWP implementation — no #ifdef XWR_SYNTAX_CHECK
// skips for the CoreWindow/event/poll logic. Instead, winheaders/WinRTStubs.h
// provides stub types so g++ can syntax-check the real code paths.
//
// Pipeline:
//   1) LoadConfig — read run.config (exe path, args, dll search paths)
//   2) Initialize:
//      - Touch ShimRegistry singleton (fires all REGISTER_SHIM static inits)
//      - Set PathTranslator's physical root from ApplicationData::Current().LocalFolder
//      - Get CoreWindow for this thread, query ICoreWindowInterop for HWND
//      - Subscribe KeyDown/KeyUp/CharacterReceived → KeyboardBridge
//      - Initialize D3D11Bridge with the HWND
//      - Start XInput polling thread (250Hz)
//   3) Run:
//      - Build PeLoader, wire to ShimRegistry
//      - Spawn game .exe on a background thread
//      - Run Win32 message pump on this thread (drives Present each frame)
//      - When game thread exits, post WM_QUIT

